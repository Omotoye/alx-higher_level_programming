#!/usr/bin/python3


def number_keys(a_dictionary) -> int:
    """A function that returns the number of keys in a dictionary

    Type Alias:
        Dictionary = dict[str, Any]

    Args:
        a_dictionary (Dictionary): dictionary to get number of keys
            from

    Returns:
        int: the count of keys in the given dictionary
    """
    return len(a_dictionary)
